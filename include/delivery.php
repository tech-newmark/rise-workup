<h1>Доставка</h1>
<p>
	 Компания RISE работает с оптовыми заказами сумок, рюкзаков, шопперов и аксессуаров. После оформления заявки менеджер согласует состав заказа, сумму, способ получения и удобную транспортную компанию для отправки в ваш город или регион.
</p>
<p>

</p>
<h2>Доставка по Санкт-Петербургу до транспортной компании</h2>
<p>
	 Для клиентов из других городов мы можем передать заказ до терминала транспортной компании в Санкт-Петербурге. Доставка до терминала выбранного перевозчика по Санкт-Петербургу выполняется бесплатно.
</p>
<p>
	 Транспортную компанию выбирает клиент. Это может быть удобный для вас перевозчик, с которым вы уже работаете, либо компания, подходящая по срокам, тарифам и географии доставки. После согласования заказа менеджер уточнит данные для отправки и передаст продукцию в выбранный терминал.
</p>
<h3>Доставка в регионы России</h3>
<p>
	 Перевозка заказа из Санкт-Петербурга в другой город или регион оплачивается клиентом отдельно по тарифам выбранной транспортной компании. Стоимость зависит от направления, объема груза, веса, условий перевозки и дополнительных услуг перевозчика.
</p>
<p>
	 RISE не включает услуги транспортной компании в стоимость товаров. Оплата доставки между городами производится клиентом напрямую перевозчику или по условиям, согласованным при оформлении отправки.
</p>
<h3>Самовывоз со склада RISE</h3>
<p>
	 Также возможен самовывоз со склада компании RISE. Этот вариант подходит клиентам из Санкт-Петербурга и тем, кто самостоятельно организует забор заказа через своего представителя или курьерскую службу.
</p>
<p>
	 Самовывоз выполняется по предварительному согласованию с менеджером. Перед приездом необходимо подтвердить готовность заказа, дату и ориентировочное время получения.
</p>
<h3>Минимальная сумма оптового заказа</h3>
<p>
	 Минимальная сумма оптового заказа составляет 15 000 рублей. После получения заявки менеджер проверит выбранные позиции, уточнит наличие, параметры заказа и подготовит информацию по дальнейшему оформлению.
</p>
<h3>Как проходит получение заказа</h3>
<ul>
	<li>Вы отправляете заявку или связываетесь с менеджером RISE.</li>
	<li>Менеджер согласует состав заказа, сумму и способ получения.</li>
	<li>Вы выбираете транспортную компанию или самовывоз со склада.</li>
	<li>Заказ передается до терминала в Санкт-Петербурге или выдается со склада по согласованию.</li>
	<li>Доставка в другой город оплачивается по тарифам выбранного перевозчика.</li>
</ul>
<h3>Что важно уточнить перед отправкой</h3>
<p>
	 Перед оформлением доставки рекомендуем заранее сообщить менеджеру город получения, выбранную транспортную компанию, данные получателя и пожелания по способу отправки. Это позволит быстрее согласовать передачу заказа и избежать ошибок в документах.
</p>