<h1>Гарантия и возврат</h1>
<p>
	 Компания RISE отвечает за качество выпускаемых сумок, рюкзаков, чемоданов, портфелей и аксессуаров. При выявлении производственного брака возможен обмен товара или возврат денежных средств в установленном порядке.
</p>
<h2>Гарантии изготовителя</h2>
<p>
	 На товары действуют гарантийные сроки эксплуатации со дня розничной продажи согласно ГОСТ 28631-2005:
</p>
<ul>
	<li>50 дней — сумки женские, мужские, дорожные и спортивные;</li>
	<li>30 дней — сумки хозяйственные, пляжные, для учащихся, портфели ученические, ранцы, изделия мелкой кожгалантереи;</li>
	<li>70 дней — чемоданы, портфели деловые и дорожные, папки.</li>
</ul>
<p>
	 Гарантия распространяется на недостатки производственного характера, выявленные в течение гарантийного срока при соблюдении правил эксплуатации, хранения и транспортировки изделия.
</p>
<p>
	 Гарантия не распространяется на дефекты, возникшие не по вине производителя: в результате механических повреждений, ударов, неправильного хранения, небрежного обращения, превышения допустимой нагрузки, использования изделия не по назначению, повреждений при перевозке третьими лицами, воздействия экстремальных температур, растворителей, кислот, длительного контакта с водой, если изделие не предназначено для таких условий, а также на царапины и следы естественного износа.
</p>
<h2>Максимальная нагрузка</h2>
<p>
	 Чтобы изделие сохраняло форму, прочность швов и исправность фурнитуры, важно соблюдать допустимую нагрузку:
</p>
<ul>
	<li>женские сумки — до 4 кг;</li>
	<li>дорожные и спортивные сумки — до 17 кг;</li>
	<li>городские рюкзаки и школьные ранцы — до 7 кг;</li>
	<li>туристические рюкзаки — до 25 кг;</li>
	<li>чемоданы на колесах — до 25 кг.</li>
</ul>
<p>
	 Если для конкретной модели указаны отдельные рекомендации по эксплуатации, необходимо ориентироваться на них.
</p>
<h2>Возврат товара надлежащего качества</h2>
<p>
	 Компания RISE предоставляет возможность вернуть товар надлежащего качества или обменять его на аналогичный в течение 14 календарных дней со дня получения заказа, если товар не подошел по форме, габаритам, фасону, расцветке, размеру или комплектации.
</p>
<p>
	 Возврат или обмен возможен, если товар не был в эксплуатации, сохранены его товарный вид, потребительские свойства, упаковка, пломбы, ярлыки и документы, подтверждающие покупку.
</p>
<p>
	 Если на день обращения аналогичный товар отсутствует, покупатель вправе отказаться от исполнения договора купли-продажи и потребовать возврата уплаченной за товар суммы.
</p>
<p>
	 Обратите внимание: при возврате товара надлежащего качества расходы на доставку товара до склада оплачивает покупатель, если иное не согласовано отдельно.
</p>
<h2>Возврат товара ненадлежащего качества</h2>
<p>
	 В течение гарантийного срока покупатель вправе обратиться по вопросу возврата или обмена товара ненадлежащего качества. Под товаром ненадлежащего качества понимается изделие с производственным браком или недостатком, возникшим не по вине покупателя.
</p>
<p>
	 При обращении необходимо предоставить товар, документы, подтверждающие покупку, а также описание выявленного недостатка. После проверки качества компания принимает решение об обмене, возврате денежных средств или ином способе урегулирования ситуации в соответствии с законодательством.
</p>
<p>
	 Если причина возникновения недостатка неочевидна и требуется установить, является ли дефект производственным или возник вследствие неправильной эксплуатации, может быть проведена экспертиза.
</p>
<h2>Как обратиться по гарантии или возврату</h2>
<p>
	 Для оформления обращения свяжитесь со специалистами RISE и сообщите номер заказа, дату покупки, наименование товара и причину обращения. При наличии дефекта желательно приложить фотографии изделия, чтобы предварительно оценить ситуацию и быстрее согласовать дальнейшие действия.
</p>
<p>
	 Все обращения рассматриваются индивидуально с учетом состояния товара, срока обращения, условий эксплуатации и требований действующего законодательства.
</p>