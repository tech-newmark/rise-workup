<h1>Оплата</h1>
<p>
	 Компания RISE предлагает удобные способы оплаты для розничных и оптовых покупателей. Условия зависят от формата заказа, статуса клиента и выбранного способа оформления покупки.
</p>
<h2>Оплата через сайт</h2>
<p>
	 Розничные покупатели могут оплатить заказ через сайт после выбора товаров в каталоге и оформления корзины. Онлайн-оплата доступна для товаров, представленных на сайте, включая сумки, рюкзаки, шопперы, аксессуары и другие изделия RISE.
</p>
<p>
	 После оформления заказа система предложит доступный способ оплаты. Перед оплатой рекомендуется дождаться подтверждения о наличии товара на складе, проверить состав заказа, количество товаров, контактные данные и условия доставки.
</p>
<h2>Безналичная оплата для оптовых покупателей</h2>
<p>
	 Для оптовых покупателей доступна только безналичная оплата. После согласования ассортимента, количества изделий и условий поставки менеджер RISE подготовит счет на оплату.
</p>
<p>
	 Оптовый заказ передается в обработку после подтверждения условий и поступления оплаты по счету, если иной порядок не согласован отдельно. Все необходимые закрывающие документы предоставляются в установленном порядке.
</p>
<h3>Порядок оплаты заказа</h3>
<ol>
	<li>Вы выбираете товары на сайте или согласовываете оптовый заказ с менеджером.</li>
	<li>Проверяете состав заказа, количество, стоимость и условия доставки.</li>
	<li>Розничный заказ оплачивается через сайт доступным способом.</li>
	<li>Для оптового заказа компания выставляет счет на безналичную оплату.</li>
	<li>После подтверждения оплаты заказ передается в дальнейшую обработку.</li>
</ol>
<h3>Документы по оплате</h3>
<p>
	 Для юридических лиц и индивидуальных предпринимателей компания RISE предоставляет необходимые документы по заказу. При оформлении сотрудничества могут потребоваться реквизиты организации или ИП, контактные данные ответственного лица и карточка клиента.
</p>
<p>
	 Если у вас возникли вопросы по оплате, счету, подтверждению платежа или документам, свяжитесь со специалистами RISE. Менеджер уточнит детали заказа, подскажет порядок оплаты и поможет согласовать условия сотрудничества.
</p>